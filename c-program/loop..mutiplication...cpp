// write a program to display multiplication of two input number without using * operator.
//119.
#include<stdio.h>
int main()
{
	int x,y,product=0;
	
	
	printf("enter the x values :");
	scanf("%d",&x);
	
	printf("enter the y values :");
	scanf("%d",&y);
	
	for(int i=0;i>=y;i++)
	{
		product=product + x;
		
	}
	printf("%d",product);
	
	
}
